# Ejemplos de Notaciones de Multiplicidad
# 1: Exactamente una instancia.
# 0..1: Cero o una instancia.
# *0.. o ***: Cero o más instancias.
# 1.. o 1..n*: Al menos una instancia, posiblemente más.
# n..m: Un rango específico, por ejemplo, 2..4 indica entre dos y cuatro instancias.




